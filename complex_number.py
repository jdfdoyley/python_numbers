# Complex Numbers
# A complex number is specified as real_part + imaginary_part, where the
# imaginary_part is written with a j or J

# Following numbers are complex numbers
x = 2j
y = 3+4j

# To extract real and imaginary parts from a complex number x, use x.real and
# x.imag

x = 3+4j

# real part
print(x.real)       # 3.0

# imaginary part
print(x.imag)       # 4.0
